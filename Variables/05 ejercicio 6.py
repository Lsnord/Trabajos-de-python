# Programa que calcula 30, 60 y 90% de numero ingresado
numero = float(input("ingresa cualquier numero"))
calculo1 = numero / 100 * 30 
calculo2 = numero / 100 * 60
calculo3 = numero / 100 * 90 
print ("el 30% de", numero, "es: ", calculo1)
print ("el 60% de", numero, "es: ", calculo2)
print ("el 90% de", numero, "es: ", calculo3)
